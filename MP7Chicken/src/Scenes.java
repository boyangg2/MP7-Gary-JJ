	public static void pr(String s) {
		wait(-1);
		System.out.println(s);
	}

	public static void wait(int a) {
		int len = a * 1000;
		if (a == -1) {
			len = 35;
		}
		long t0, t1;
		t0 = System.currentTimeMillis();
		do { 
			t1 = System.currentTimeMillis();
		} while((t1 - t0) < len);
	}

